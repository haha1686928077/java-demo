//用for来实现1-5和5-1


public class Testfor2 {
    public static void main(String[] args) {
        for (int i = 0; i <= 5; i++) {
            System.out.println(i);
        }

        System.out.println("..................");

        for (int j = 5; j >= 0; j--) {
            System.out.println(j);
        }
    }
}
